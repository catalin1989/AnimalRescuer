package org.example;

public class Animal {

    String name;
    int age;
    int health;
    int hunger;
    int happiness;
    String favouriteFood;
    String favouriteActivity;

    public Animal(String name, int age, int health, int hunger, int happiness, String favouriteFood, String favouriteActivity) {
        this.name = name;
        this.age = age;
        this.health = health;
        this.hunger = hunger;
        this.happiness = happiness;
        this.favouriteFood = favouriteFood;
        this.favouriteActivity = favouriteActivity;
    }
}
